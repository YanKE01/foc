void HAL_ADCEx_InjectedConvCpltCallback(ADC_HandleTypeDef *hadc)
{
  UNUSED(hadc);
	static int cnt=0;
	if(hadc==&hadc1)
	{
		if(adc_offset_flag==0)
		{
			cnt++;
			adc1_in5_a = hadc1.Instance->JDR1;
			adc1_in14_b = hadc1.Instance->JDR2;
			adc1_in11_c = hadc1.Instance->JDR3;
			ia_offset+=adc1_in5_a;
			ib_offset+=adc1_in14_b;
			ic_offset+=adc1_in11_c;
			if (cnt >= 10)
      {
        adc_offset_flag = 1;
        ia_offset /= 10;
        ib_offset /= 10;
        ic_offset /= 10;
      }
		}else{
			adc1_in5_a = hadc1.Instance->JDR1;
			ia = (adc1_in5_a-ia_offset)*0.00460491889f;
			adc1_in14_b = hadc1.Instance->JDR2;
			ib = (adc1_in14_b-ib_offset)*0.00460491889f;
			adc1_in11_c = hadc1.Instance->JDR3;		
			ic = (adc1_in11_c-ic_offset)*0.00460491889f;			
			
			VF_step();
			TIM1->CCR1 = rtY.tABC[0];
			TIM1->CCR2 = rtY.tABC[1];
			TIM1->CCR3 = rtY.tABC[2];

			temp[0] = ia;
			temp[1] = ib;
			temp[2] = ic;
			temp[3] = vbus;
			temp[4] = rtY.tABC[0];
			temp[5] = rtY.tABC[1];
			temp[6] = rtY.tABC[2];
      memcpy(tx_buffer, (uint8_t *)&temp, sizeof(temp));
      HAL_UART_Transmit_DMA(&huart1, (uint8_t *)tx_buffer, sizeof(tx_buffer));
		}
	}
	
}
